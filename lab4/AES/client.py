RCON = [0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1B, 0x36]


def bytes_to_hex(b: bytes) -> str:
    return ' '.join(f'{x:02X}' for x in b)


def state_to_hex(state: List[List[int]]) -> str:
    lines = []
    for r in range(4):
        line = ' '.join(f'{state[r][c]:02X}' for c in range(4))
        lines.append(line)
    return '  '.join(lines)


def bytes_to_state(block: bytes) -> List[List[int]]:
    state = [[0] * 4 for _ in range(4)]
    for i in range(16):
        state[i % 4][i // 4] = block[i]
    return state


def state_to_bytes(state: List[List[int]]) -> bytes:
    out = bytearray(16)
    for i in range(16):
        out[i] = state[i % 4][i // 4]
    return bytes(out)


def mul(a: int, b: int) -> int:
    p = 0
    aa = a & 0xFF
    bb = b & 0xFF
    for _ in range(8):
        if bb & 1:
            p ^= aa
        hi = aa & 0x80
        aa = (aa << 1) & 0xFF
        if hi:
            aa ^= 0x1B
        bb >>= 1
    return p


def sub_bytes(state):
    for r in range(4):
        for c in range(4):
            state[r][c] = SBOX[state[r][c]]


def inv_sub_bytes(state):
    for r in range(4):
        for c in range(4):
            state[r][c] = INV_SBOX[state[r][c]]


def shift_rows(state):
    for r in range(1, 4):
        state[r] = state[r][r:] + state[r][:r]


def inv_shift_rows(state):
    for r in range(1, 4):
        state[r] = state[r][-r:] + state[r][:-r]


def mix_columns(state):
    for c in range(4):
        a0, a1, a2, a3 = [state[r][c] for r in range(4)]
        state[0][c] = mul(0x02, a0) ^ mul(0x03, a1) ^ a2 ^ a3
        state[1][c] = a0 ^ mul(0x02, a1) ^ mul(0x03, a2) ^ a3
        state[2][c] = a0 ^ a1 ^ mul(0x02, a2) ^ mul(0x03, a3)
        state[3][c] = mul(0x03, a0) ^ a1 ^ a2 ^ mul(0x02, a3)


def inv_mix_columns(state):
    for c in range(4):
        a0, a1, a2, a3 = [state[r][c] for r in range(4)]
        state[0][c] = mul(0x0e, a0) ^ mul(0x0b, a1) ^ mul(0x0d, a2) ^ mul(0x09, a3)
        state[1][c] = mul(0x09, a0) ^ mul(0x0e, a1) ^ mul(0x0b, a2) ^ mul(0x0d, a3)
        state[2][c] = mul(0x0d, a0) ^ mul(0x09, a1) ^ mul(0x0e, a2) ^ mul(0x0b, a3)
        state[3][c] = mul(0x0b, a0) ^ mul(0x0d, a1) ^ mul(0x09, a2) ^ mul(0x0e, a3)


def add_round_key(state, round_key: bytes):
    for c in range(4):
        for r in range(4):
            state[r][c] ^= round_key[c * 4 + r]


def key_expansion(key: bytes) -> bytes:
    expanded = bytearray(16 * (Nr + 1))
    expanded[:16] = key

    bytes_generated = 16
    rcon_i = 1

    while bytes_generated < len(expanded):
        temp = expanded[bytes_generated - 4:bytes_generated]

        if bytes_generated % 16 == 0:
            # RotWord
            temp = temp[1:] + temp[:1]
            # SubWord
            temp = bytes(SBOX[b] for b in temp)
            # XOR with Rcon
            temp = bytes([temp[0] ^ RCON[rcon_i]] + list(temp[1:]))
            rcon_i += 1

        for i in range(4):
            expanded[bytes_generated] = expanded[bytes_generated - 16] ^ temp[i]
            bytes_generated += 1

    return bytes(expanded)


def decrypt_block(cipher_block: bytes, expanded_key: bytes, block_index: int) -> bytes:
    state = bytes_to_state(cipher_block)
    print(f"\n--- Decrypting block {block_index} ---")
    print("Initial State (ciphertext block): " + state_to_hex(state))

    # Initial AddRoundKey (last round key)
    add_round_key(state, expanded_key[Nr * 16:(Nr + 1) * 16])
    print(f"After AddRoundKey (Round {Nr}): " + state_to_hex(state))

    for round in range(Nr - 1, 0, -1):
        inv_shift_rows(state)
        print(f"Round {round} - After InvShiftRows:     " + state_to_hex(state))

        inv_sub_bytes(state)
        print(f"Round {round} - After InvSubBytes:      " + state_to_hex(state))

        add_round_key(state, expanded_key[round * 16:(round + 1) * 16])
        print(f"Round {round} - After AddRoundKey:      " + state_to_hex(state))

        inv_mix_columns(state)
        print(f"Round {round} - After InvMixColumns:    " + state_to_hex(state))

    # Final round (no InvMixColumns)
    inv_shift_rows(state)
    print("Round 0 - After InvShiftRows:      " + state_to_hex(state))

    inv_sub_bytes(state)
    print("Round 0 - After InvSubBytes:       " + state_to_hex(state))

    add_round_key(state, expanded_key[0:16])
    print("Round 0 - After AddRoundKey (plaintext): " + state_to_hex(state))

    return state_to_bytes(state)


def decrypt(ciphertext: bytes, key: bytes) -> bytes:
    if len(ciphertext) % 16 != 0:
        raise ValueError("Ciphertext length must be multiple of 16 bytes")

    expanded_key = key_expansion(key)
    plaintext = bytearray()

    for i in range(0, len(ciphertext), 16):
        block = ciphertext[i:i + 16]
        dec_block = decrypt_block(block, expanded_key, i // 16)
        plaintext.extend(dec_block)

    # Remove PKCS#7 padding
    pad_len = plaintext[-1]
    if 1 <= pad_len <= 16:
        if all(plaintext[-j] == pad_len for j in range(1, pad_len + 1)):
            plaintext = plaintext[:-pad_len]

    return bytes(plaintext)


def parse_key(key_str: str) -> bytes:
    kb = key_str.encode('utf-8')
    if len(kb) >= 16:
        return kb[:16]
    else:
        return kb + b'\x00' * (16 - len(kb))


def main():
    PORT = 5001
    print(f"Receiver listening on port {PORT}")

    server = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
    server.bind(('localhost', PORT))
    server.listen(1)

    conn, addr = server.accept()
    print(f"Connected by {addr}")

    with conn:
        # Read length + ciphertext
        cipher_len = int.from_bytes(conn.recv(4), 'big')
        cipher_bytes = conn.recv(cipher_len)
        cipher_str = cipher_bytes.decode('utf-8', errors='replace')
        print("Cipher received:", repr(cipher_str))

        key = input("Please enter key to decrypt: ").strip()
        print()

        key_bytes = parse_key(key)
        try:
            plaintext_bytes = decrypt(cipher_bytes, key_bytes)
            plaintext = plaintext_bytes.decode('utf-8', errors='replace')
            print("\nDecrypted plaintext:", plaintext)

            ack = f"OK: decrypted {len(plaintext)} chars"
            ack_bytes = ack.encode('utf-8')
            conn.send(len(ack_bytes).to_bytes(4, 'big'))
            conn.send(ack_bytes)
        except Exception as e:
            print("Decryption failed:", e)
            conn.send(b"ERROR")


if __name__ == "__main__":
    main()